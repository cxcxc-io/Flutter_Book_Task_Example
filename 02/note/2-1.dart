void main() {
  
    // 有一個以文字表達的變數，其變數名為 name，內容叫做小菜。
    String name = "小菜";

    // 有一個以文字表達的變數，其變數名為 companyName，內容叫做雲育鏈。
    String companyName = "雲育鏈";

    // 有一個以數字表達的變數，其變數名為 age，內容為 24。
    int age = 24;

    // 將內容印出來
    print(name);
    print(name + "在" + companyName);

    // 將數字的內容印出來
    print(age.toString);

    // 將內容組合，並列印
    print("有一個年紀為" + age.toString() + "歲的男孩子在" + companyName + "，他的名字叫做" + name);

}
